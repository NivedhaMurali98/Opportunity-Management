package com.jackrutorial.models;

//POJO for skills entity
public class Skillset
{
    public Integer skid;
    public String title;
    public Integer getSkid()
    {
        return skid;
    }
    public void setSkid(Integer skid)
    {
        this.skid = skid;
    }
    public String getTitle()
    {
        return title;
    }
    public void setTitle(String title)
    {
        this.title = title;
    }

}
